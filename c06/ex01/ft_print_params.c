#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	p;

	p = 1;
	while (p < ac)
	{
		i = 0;
		while (av[p][i] != '\0')
		{
			write(1, &av[p][i], 1);
			i++;
		}
		write(1, "\n", 1);
		p++;
	}
	return (0);
}
