#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	p;

	p = ac - 1;
	while (p > 0)
	{
		i = 0;
		while (av[p][i] != '\0')
		{
			write(1, &av[p][i], 1);
			i++;
		}
		p--;
		write(1, "\n", 1);
	}
	return (0);
}
