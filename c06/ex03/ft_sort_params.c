#include <unistd.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] != '\0' && s1[i] == s2[i])
	{
		i++;
	}
	return (s1[i] - s2[i]);
}

void	ft_swap(char **a, char **b)
{
	char	*temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write (1, str, 1);
		str++;
	}
}

int	main(int ac, char **av)
{
	int	j;
	int	con;

	con = 1;
	while (con < (ac - 1))
	{
		j = 1;
		while (j < (ac - 1))
		{
			if ((ft_strcmp(av[j], av[j + 1])) > 0)
				ft_swap(&av[j], &av[j + 1]);
			j++;
		}
		con++;
	}
	j = 1;
	while (j < ac)
	{
		ft_putstr(av[j]);
		write(1, "\n", 1);
		j++;
	}
	return (0);
}
