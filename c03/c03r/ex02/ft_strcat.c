char	*ft_strcat(char *dest, char *src)
{
	int	d;
	int	i;

	i = 0;
	d = 0;
	while (dest[d] != '\0')
		d++;
	while (src[i] != '\0')
	{
		dest[d + i] = src[i];
		i++;
	}
	dest[d + i] = '\0';
	return (dest);
}
