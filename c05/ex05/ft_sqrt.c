#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	q;

	q = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (q * q != nb)
	{
		if (q * q > nb)
		{
			return (0);
		}
		q++;
	}
	return (q);
}
